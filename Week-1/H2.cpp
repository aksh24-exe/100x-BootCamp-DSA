#include<iostream>
using namespace std;

int main(){
    long long int N, M;
    cin>>N>>M;
    cout<<N<<" + "<<M<<" = "<<N+M<<endl;
    cout<<endl;
    cout<<N<<" - "<<M<<" = "<<N-M<<endl;
    cout<<endl;
    cout<<N<<" * "<<M<<" = "<<N*M<<endl;
    cout<<endl;
    cout<<N<<" / "<<M<<" = "<<N/M<<endl;
    cout<<endl;
    cout<<N<<" % "<<M<<" = "<<N%M<<endl;
    return 0;
}